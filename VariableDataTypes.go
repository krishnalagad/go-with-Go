package main

import "fmt"

func main() {
	fmt.Println("Variables and Data Types")

	var name string = "Krishna Lagad" // explicitly specifying datatype
	fmt.Println("My name is", name)

	var age uint8 = 22
	fmt.Println(age)

	var mob uint64 = 9876452345
	fmt.Println(mob)

	var isTrue bool = false
	fmt.Println(isTrue)
}
